package esoterics

import "testing"

func TestPlusMinusEquals(t *testing.T) {
	cases := []struct{ input, expected string }{
		{"++-=-", "1\n"},
		{"++++++++++++++++++++++++++++++++++++++++++=", "42\n"},
		{"+=+=+=+=+=+=+=+=+=+=", "1\n2\n3\n4\n5\n6\n7\n8\n9\n10\n"},
		{"++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++=+++++++++++++++++++++++++++++=+++++++==+++=-------------------------------------------------------------------=------------=+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++=--------=+++=------=--------=-------------------------------------------------------------------=-----------------------=", "72\n101\n108\n108\n111\n44\n32\n119\n111\n114\n108\n100\n33\n10\n"},
	}

	for _, c := range cases {
		actual := PlusMinusEquals(c.input)
		if actual != c.expected {
			t.Errorf("Test failed, expected: '%s', got:  '%s'", c.expected, actual)
		}
	}
}
